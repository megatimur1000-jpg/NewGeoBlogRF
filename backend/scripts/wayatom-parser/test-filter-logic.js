// Тест логики фильтрации меток
const parser = require('./parsers/cityParser');

// Тестовые метки
const testMarkers = [
  { name: 'Хинкальный дом' },
  { name: 'Вкусно и точка' },
  { name: 'Кафе техцентра Гранд' },
  { name: 'Дом' }, // Должен быть отклонен
  { name: 'Кафе' }, // Должен быть отклонен
  { name: 'Точка' }, // Должен быть отклонен
  { name: 'Ресторан Пушкин' }, // Должен пройти
  { name: 'Отель Москва' }, // Должен пройти
  { name: 'Парк Горького' }, // Должен пройти
  { name: 'Магазин продуктов' }, // Должен пройти
  { name: 'Без названия' }, // Должен быть отклонен
  { name: 'Неизвестно' }, // Должен быть отклонен
  { name: '123' }, // Должен быть отклонен
  { name: 'А' }, // Должен быть отклонен (слишком короткое)
];

console.log('Тестирование логики фильтрации меток:\n');

testMarkers.forEach(marker => {
  const isQuality = parser.isQualityMarker(marker);
  const status = isQuality ? '✅ ПРОЙДЕТ' : '❌ ОТКЛОНЕН';
  console.log(`${status}: "${marker.name}"`);
});

console.log('\nОбъяснение логики:');
console.log('- Метки с дополнительными словами (например, "Хинкальный дом") теперь проходят');
console.log('- Метки состоящие ТОЛЬКО из общих слов (например, "Дом") отклоняются');
console.log('- Метки с неполной информацией (например, "Без названия") отклоняются');
