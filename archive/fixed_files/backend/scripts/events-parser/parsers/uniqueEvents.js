// Функция для получения уникальных событий для города
function getUniqueEventsForCity(cityName) {
  if (!cityName || typeof cityName !== 'string') {
    return null;
  }
  
  // Нормализуем название города (убираем пробелы, приводим к единому виду)
  const normalize = (name) => name.toLowerCase().trim().replace(/\s+/g, ' ');
  
  const normalizedInput = normalize(cityName);
  
  // Прямое совпадение
  if (module.exports[cityName]) {
    return module.exports[cityName];
  }
  
  // Поиск по нормализованному названию
  const cityKeys = Object.keys(module.exports).filter(k => 
    k !== 'getUniqueEventsForCity' && 
    k !== 'getAllCitiesWithEvents' &&
    Array.isArray(module.exports[k])
  );
  
  for (const key of cityKeys) {
    const normalizedKey = normalize(key);
    // Точное совпадение после нормализации
    if (normalizedInput === normalizedKey) {
      return module.exports[key];
    }
    // Частичное совпадение - только если одно название полностью содержится в другом
    // И длина совпадения не менее 5 символов (чтобы избежать ложных совпадений)
    if (normalizedInput.length >= 5 && normalizedKey.length >= 5) {
      if (normalizedInput.includes(normalizedKey) && normalizedKey.length >= 5) {
        return module.exports[key];
      }
      if (normalizedKey.includes(normalizedInput) && normalizedInput.length >= 5) {
        return module.exports[key];
      }
    }
  }
  
  // Специальные случаи для городов с составными названиями
  const specialCases = {
    'ростов': 'Ростов-на-Дону',
    'ростов-на-дону': 'Ростов-на-Дону',
    'нижний новгород': 'Нижний Новгород',
    'санкт-петербург': 'Санкт-Петербург',
    'спб': 'Санкт-Петербург',
    'питер': 'Санкт-Петербург'
  };
  
  const specialKey = specialCases[normalizedInput];
  if (specialKey && module.exports[specialKey]) {
    return module.exports[specialKey];
  }
  
  return null;
}

// Функция для получения всех городов с событиями
function getAllCitiesWithEvents() {
  const cityKeys = Object.keys(module.exports).filter(k => 
    k !== 'getUniqueEventsForCity' && 
    k !== 'getAllCitiesWithEvents' &&
    Array.isArray(module.exports[k]) // Проверяем, что это массив событий
  );
  return cityKeys.map(key => ({
    name: key,
    events: module.exports[key]
  }));
}

module.exports.getUniqueEventsForCity = getUniqueEventsForCity;
module.exports.getAllCitiesWithEvents = getAllCitiesWithEvents;

